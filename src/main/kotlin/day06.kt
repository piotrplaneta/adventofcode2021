val cache = mutableMapOf<Pair<Int, Int>, Long>()

private fun amountOfLanterns(internalTimer: Int, day: Int): Long {
    return if (internalTimer >= day) {
        1
    } else {
        var daysLeft = day - internalTimer - 1
        var newLanterns: Long = 1
        while (daysLeft > 0) {
            newLanterns += cache.getOrPut(Pair(internalTimer, daysLeft)) { amountOfLanterns(8, daysLeft) }
            daysLeft -= 7
        }

        newLanterns
    }
}

